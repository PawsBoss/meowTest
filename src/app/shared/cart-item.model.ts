export class CartItem {

    public amount: string;
    public price: number;
    public phone: string;
    public frequency: string;
    public secret: string;

    constructor(amount: string, price: number, phone: string, frequency: string, secret: string) {

        this.amount = amount;
        this.price = price;
        this.phone = phone;
        this.frequency = frequency;
        this.secret = secret;

    }





}
